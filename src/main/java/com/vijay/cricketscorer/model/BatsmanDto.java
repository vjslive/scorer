package com.vijay.cricketscorer.model;

public class BatsmanDto {

	private Integer batsmanNo;
	private String batsman;
	private Integer runstaken;
	private Integer balls;
	private Integer fours;
	private Integer sixes;
	private String outBy;
	private String bowler;
	private String fielder;
	private Integer battingorder;
	
	public Integer getBatsmanNo() {
		return batsmanNo;
	}
	public void setBatsmanNo(Integer batsmanNo) {
		this.batsmanNo = batsmanNo;
	}
	public String getBatsman() {
		return batsman;
	}
	public void setBatsman(String batsman) {
		this.batsman = batsman;
	}
	public Integer getRunstaken() {
		return runstaken;
	}
	public void setRunstaken(Integer runstaken) {
		this.runstaken = runstaken;
	}
	public Integer getBalls() {
		return balls;
	}
	public void setBalls(Integer balls) {
		this.balls = balls;
	}
	public Integer getFours() {
		return fours;
	}
	public void setFours(Integer fours) {
		this.fours = fours;
	}
	public Integer getSixes() {
		return sixes;
	}
	public void setSixes(Integer sixes) {
		this.sixes = sixes;
	}
	public String getOutBy() {
		return outBy;
	}
	public void setOutBy(String outBy) {
		this.outBy = outBy;
	}
	public String getBowler() {
		return bowler;
	}
	public void setBowler(String bowler) {
		this.bowler = bowler;
	}
	public String getFielder() {
		return fielder;
	}
	public void setFielder(String fielder) {
		this.fielder = fielder;
	}
	public Integer getBattingorder() {
		return battingorder;
	}
	public void setBattingorder(Integer battingorder) {
		this.battingorder = battingorder;
	}
	
}
